public class Single {
    String singleTitle;
    String singleID;
    String Artist;
    String Type;
    int singleDuration;

    public Single (String singleTitle, String singleID, String Artist, String Type, int Duration){
        this.singleTitle = singleTitle;
        this.singleID = singleID;
        this.Artist = Artist;
        this.Type = Type;
        this.singleDuration = Duration;
    }
}
