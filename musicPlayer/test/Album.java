public class Album {
    private String Title;
    private String ID;
    private String Artist;
    private String Songs;
    private String Copyright;
    private int Duration;

    public Album(String Title, String ID, String Artist, String Songs, String Copyright, int Duration) {
        this.Title = Title;
        this.ID = ID;
        this.Artist = Artist;
        this.Songs = Songs;
        this.Copyright = Copyright;
        this.Duration = Duration;
    }
}
